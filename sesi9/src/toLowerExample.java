public class toLowerExample {
    public static void main(String[] args) {
        String stringOri = "Welcome to Mangcoding";
        String stringLower = stringOri.toLowerCase();
        System.out.println("Before using the toLowerCase method: " + stringOri);
        System.out.println("After using the toLowerCase method: " + stringLower);
    }
}

// Output:
// Before using the toLowerCase method: Welcome to Mangcoding
// After using the toLowerCase method: welcome to mangcoding




